uint32_t debug_cdc_bytes_rx;

//testing 2 endpoints, outputs to other endpoint, limited to 64 bytes.
// otherwise use polling in main() to wait for TX empty.
void USB_CDC_RxHandler(uint8_t *Buf, uint32_t Len, uint16_t ep_out) {

	debug_cdc_bytes_rx += Len;

	if(ep_out == CDC_OUT_EP1){
		HAL_GPIO_TogglePin(LD6_GPIO_Port, LD6_Pin); //blue COM10
	}
	else{
		HAL_GPIO_TogglePin(LD4_GPIO_Port, LD4_Pin); //ora  COM11
	}

	//convert to EP_IN_xx for "other" ep
	uint16_t ep_in = (ep_out == CDC_OUT_EP1) ? CDC_IN_EP2 : CDC_IN_EP1;

	if( USBD_OK != CDC_Transmit_FS( Buf, Len, ep_in ) ){
		HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, GPIO_PIN_SET);  //red if busy
	}else{
		HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, GPIO_PIN_RESET);
	}

}
